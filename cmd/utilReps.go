// bind forge replace values
func repsForge() []rep {
	// sanity check
	if len(options.Old) != len(options.New) {
		log.Fatal("Replace values do not match")
	}

	// preallocate
	reps := make([]rep, len(options.Old))
	for į := range options.Old {
		reps[į] = rep{old: options.Old[į], new: options.New[į]}
	}
	return reps
}

