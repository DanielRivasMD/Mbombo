// bind forge replace values
func repsForge() []rep {
	// sanity check
	if len(old) != len(new) {
		log.Fatal("Replace values do not match")
	}

	// preallocate
	reps := make([]rep, len(old))
	for i, _ := range old {
		reps[i] = rep{old: old[i], new: new[i]}
	}
	return reps
}

