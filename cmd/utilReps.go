// bind forge replace values
func repsForge() []rep {
	// sanity check
	if len(old) != len(new) {
		log.Fatal("Replace values do not match")
	}

	// preallocate
	reps := make([]rep, len(old))
	for į := range old {
		reps[į] = rep{old: old[į], new: new[į]}
	}
	return reps
}

