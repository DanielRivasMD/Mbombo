////////////////////////////////////////////////////////////////////////////////////////////////////

// copy & replace
type paramsCR struct {
	orig  string
	dest  string
	files []string
	reps  []rep
}

////////////////////////////////////////////////////////////////////////////////////////////////////

func newCR() paramsCR {
	params := paramsCR{}
	params.files = []string{}
	params.reps = []rep{}
	return params
}

func replaceCR(target string, reps []rep) paramsCR {
	params := paramsCR{}
	params.orig = target
	params.reps = reps
	return params
}

func copyCR(orig, dest string) paramsCR {
	params := newCR()
	params.orig = orig
	params.dest = dest
	return params
}

